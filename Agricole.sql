PRAGMA foreign_keys = ON;

DROP TABLE IF EXISTS agricole_alerte;
DROP TABLE IF EXISTS agricole_observation;
DROP TABLE IF EXISTS agricole_culture;
DROP TABLE IF EXISTS agricole_meteo;
DROP TABLE IF EXISTS agricole_parcelle;

CREATE TABLE agricole_parcelle (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nom VARCHAR(255) NOT NULL,
    localisation VARCHAR(255) NOT NULL,
    surface_ha REAL NOT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE agricole_culture (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    type VARCHAR(255) NOT NULL,
    date_semis DATE NOT NULL,
    parcelle_id INTEGER NOT NULL,
    FOREIGN KEY (parcelle_id) REFERENCES agricole_parcelle(id) ON DELETE CASCADE
);

CREATE TABLE agricole_observation (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date DATE NOT NULL,
    etat VARCHAR(255) NOT NULL,
    commentaire VARCHAR(255) NULL,
    parcelle_id INTEGER NOT NULL,
    FOREIGN KEY (parcelle_id) REFERENCES agricole_parcelle(id) ON DELETE CASCADE
);

CREATE TABLE agricole_meteo (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date DATE NOT NULL UNIQUE,
    temperature INTEGER NOT NULL,
    humidite INTEGER NOT NULL,
    pluie_mm INTEGER NOT NULL
);

CREATE TABLE agricole_alerte (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date DATE NOT NULL,
    type VARCHAR(255) NOT NULL,
    niveau VARCHAR(1) NOT NULL,
    parcelle_id INTEGER NOT NULL,
    est_lue INTEGER NOT NULL DEFAULT 0,
    FOREIGN KEY (parcelle_id) REFERENCES agricole_parcelle(id) ON DELETE CASCADE
);

